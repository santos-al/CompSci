print('-------------------------------------------------------------')
score = int(input("Score: "))

if (90 <= score <= 100): {
    print("Grade: A")
}
elif (score >= 80 and score  < 90): {
    print("Grade: B")
}
elif (score >= 70 and score < 80): {
    print("Grade: C")
}
else: {
    print("Grade: F")
}